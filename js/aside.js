function toggle_menu_1(){
    if ( document.getElementById("instituciones").style.display !== "none")
        document.getElementById("instituciones").style.display = "none";
    else document.getElementById("instituciones").style.display = "block";
}

function toggle_menu_2(){
    if ( document.getElementById("usuarios").style.display !== "none")
        document.getElementById("usuarios").style.display = "none";
    else document.getElementById("usuarios").style.display = "block";
}
